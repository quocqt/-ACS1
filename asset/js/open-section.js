// Section 1
var containerSectionFirst = document.getElementById("container__item-1");
var itemHeadingFirst = document.getElementById("item__heading-1");
var plusIconFirst = document.getElementById("heading__plus-1");
var minusIconFirst = document.getElementById("heading__minus-1");
var sectionHeightFirst = containerSectionFirst.clientHeight;

plusIconFirst.onclick = function () {
  var isOpenSectionFirst = containerSectionFirst.clientHeight == sectionHeightFirst;

  if (isOpenSectionFirst) {
    containerSectionFirst.style.height = "auto";
    plusIconFirst.style.display = "none";
    minusIconFirst.style.display = "block";
  } else {
    containerSectionFirst.style.height = null;
  }
};

minusIconFirst.onclick = function () {
  var isCloseSectionFirst = containerSectionFirst.clientHeight == sectionHeightFirst;

  if (isCloseSectionFirst) {
    containerSectionFirst.style.height = "auto";
  } else {
    containerSectionFirst.style.height = null;
    plusIconFirst.style.display = "block";
    minusIconFirst.style.display = "none";
  }
};

